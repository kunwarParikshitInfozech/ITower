package com.isl.workflow.modal;

public class CustomRule {
    private JavaScriptExpression hide;
    private JavaScriptExpression disable;
    private JavaScriptExpression customclass;

    public JavaScriptExpression getHide() {
        return hide;
    }

    public void setHide(JavaScriptExpression hide) {
        this.hide = hide;
    }

    public JavaScriptExpression getDisable() {
        return disable;
    }

    public void setDisable(JavaScriptExpression disable) {
        this.disable = disable;
    }

    public JavaScriptExpression getCustomclass() {
        return customclass;
    }

    public void setCustomclass(JavaScriptExpression customclass) {
        this.customclass = customclass;
    }
}
