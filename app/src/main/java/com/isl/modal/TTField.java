package com.isl.modal;

public class TTField {
	String fieldId;
	String caption;
	String on_add_screen;
	String on_update_screen;
	String on_detail_screen;
	String moduleId;

	public String getModuleId() {
		return moduleId;
	}

	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}

	public String getFieldId() {
		return fieldId;
	}
	public void setFieldId(String fieldId) {
		this.fieldId = fieldId;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getOn_add_screen() {
		return on_add_screen;
	}
	public void setOn_add_screen(String on_add_screen) {
		this.on_add_screen = on_add_screen;
	}
	public String getOn_update_screen() {
		return on_update_screen;
	}
	public void setOn_update_screen(String on_update_screen) {
		this.on_update_screen = on_update_screen;
	}
	public String getOn_detail_screen() {
		return on_detail_screen;
	}
	public void setOn_detail_screen(String on_detail_screen) {
		this.on_detail_screen = on_detail_screen;
	}	
	
	
}
