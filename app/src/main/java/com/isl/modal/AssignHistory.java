public class AssignHistory {

    String ch_id;
    String assigned_to;
    String created_by;
    String created_by1;
    String start_assign_date;
    String end_assign_date;

    public String getCh_id() {
        return ch_id;
    }

    public void setCh_id(String ch_id) {
        this.ch_id = ch_id;
    }

    public String getAssigned_to() {
        return assigned_to;
    }

    public void setAssigned_to(String assigned_to) {
        this.assigned_to = assigned_to;
    }

    public String getCreated_by() {
        return created_by;
    }

    public void setCreated_by(String created_by) {
        this.created_by = created_by;
    }

    public String getCreated_by1() {
        return created_by1;
    }

    public void setCreated_by1(String created_by1) {
        this.created_by1 = created_by1;
    }

    public String getStart_assign_date() {
        return start_assign_date;
    }

    public void setStart_assign_date(String start_assign_date) {
        this.start_assign_date = start_assign_date;
    }

    public String getEnd_assign_date() {
        return end_assign_date;
    }

    public void setEnd_assign_date(String end_assign_date) {
        this.end_assign_date = end_assign_date;
    }
}
