package com.isl.modal;

public class BeanSpareParts {
	String spareId;
	String spareName;
    String spareDatatype;
    String spareQty;
    String flag;
    String cryId;
    String actId;

	public String getCryId() {
		return cryId;
	}

	public void setCryId(String cryId) {
		this.cryId = cryId;
	}

	public String getActId() {
		return actId;
	}

	public void setActId(String actId) {
		this.actId = actId;
	}

	public String getSpareId() {
		return spareId;
	}
	public void setSpareId(String spareId) {
		this.spareId = spareId;
	}
	public String getSpareName() {
		return spareName;
	}
	public void setSpareName(String spareName) {
		this.spareName = spareName;
	}
	public String getSpareDatatype() {
		return spareDatatype;
	}
	public void setSpareDatatype(String spareDatatype) {
		this.spareDatatype = spareDatatype;
	}
	public String getSpareQty() {
		return spareQty;
	}
	public void setSpareQty(String spareQty) {
		this.spareQty = spareQty;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	
}
