package com.isl.modal;

public class BeansSites {
	String etsId;
	String siteId;
	String opcoSiteId;
	String siteLat;
	String siteLong;

	public String getOpcoSiteId() {
		return opcoSiteId;
	}

	public void setOpcoSiteId(String opcoSiteId) {
		this.opcoSiteId = opcoSiteId;
	}

	public String getEtsId() {
		return etsId;
	}
	public void setEtsId(String etsId) {
		this.etsId = etsId;
	}
	public String getSiteId() {
		return siteId;
	}
	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}
	public String getSiteLat() {
		return siteLat;
	}
	public void setSiteLat(String siteLat) {
		this.siteLat = siteLat;
	}
	public String getSiteLong() {
		return siteLong;
	}
	public void setSiteLong(String siteLong) {
		this.siteLong = siteLong;
	}
}
