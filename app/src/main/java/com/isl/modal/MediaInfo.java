public class MediaInfo {
    String mediaType;
    String tag;
    int flag;


    public String getMediaType() {
        return mediaType;
    }

    public void setMediaType(String mediaType) {
        this.mediaType = mediaType;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public int getFlag() {
        return flag;
    }

   public void setFlag(int flag) {
        this.flag = flag;
    }
}
